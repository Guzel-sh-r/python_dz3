# Популярные методы списка (list):
spisok = [12, 10, 4, 5, 7, 0]
spisok.append(12), spisok.count(12), spisok.index(12, 3), spisok.pop(), spisok.sort()


# Популярные методы словаря (dict):
dictionary = {'name': 'Frank', 'age': 20}
dictionary.keys(), dictionary.values(), dictionary.items(), dictionary.get('name', 'No'), dictionary.update(age=25)


# Популярные методы множества (set):
seting = {12, 10, 4, 5, 7, 0}
seting.add(12), seting.pop(), seting.discard(3), seting.update({4, 8}), seting.copy()


# Популярные методы строк (str):
stroka = 'Весело играть на лужайке'
stroka.split(), stroka.replace('Весело', 'Интересно'), stroka.count('играть'), stroka.lower(), stroka.find('е')
